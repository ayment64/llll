import 'package:llll/Features/Sign_in/Domain/Entities/User.dart';
import 'package:flutter/foundation.dart';
import 'package:meta/meta.dart';

class UserModel extends User {
  UserModel({
    @required String deviceType,
    @required String deviceToken,
    @required String deviceId,
    @required int id,
    @required String username,
    @required String usernameCanonical,
    @required String salt,
    @required String email,
    @required String emailCanonical,
    @required String password,
    @required String plainPassword,
    @required DateTime lastLogin,
    @required String confirmationToken,
    @required List roles,
    @required bool accountNonExpired,
    @required bool accountNonLocked,
    @required bool credentialsNonExpired,
    @required bool enabled,
    @required bool superAdmin,
    @required String passwordRequestedAt,
    @required List groups,
    @required List groupNames,
    @required String passwordConfirmation,
  }) : super(
            deviceType: deviceType,
            deviceToken: deviceToken,
            deviceId: deviceId,
            id: id,
            username: username,
            usernameCanonical: usernameCanonical,
            salt: salt,
            email: email,
            emailCanonical: emailCanonical,
            password: password,
            plainPassword: plainPassword,
            lastLogin: lastLogin,
            confirmationToken: confirmationToken,
            roles: roles,
            accountNonExpired: accountNonExpired,
            accountNonLocked: accountNonLocked,
            credentialsNonExpired: credentialsNonExpired,
            enabled: enabled,
            superAdmin: superAdmin,
            passwordRequestedAt: passwordRequestedAt,
            groups: groups,
            groupNames: groupNames,
            passwordConfirmation: passwordConfirmation);
  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
        deviceType: json['devicetype'],
        deviceToken: json['devicetoken'],
        deviceId: json['deviceid'],
        id: json['id'],
        username: json['username'],
        usernameCanonical: json['usernameCanonical'],
        salt: json['salt'],
        email: json['email'],
        emailCanonical: json['emailCanonical'],
        password: json['password'],
        plainPassword: json['plainPassword'],
        lastLogin: json['lastLogin'],
        confirmationToken: json['confirmationToken'],
        roles: json['roles'],
        accountNonExpired: json['accountNonExpired'],
        accountNonLocked: json['accountNonLocked'],
        credentialsNonExpired: json['credentialsNonExpired'],
        enabled: json['enabled'],
        superAdmin: json['superAdmin'],
        passwordRequestedAt: json['passwordRequestedAt'],
        groups: json['groups'],
        groupNames: json['groupNames'],
        passwordConfirmation: json['password_confirmation']);
  }
}
